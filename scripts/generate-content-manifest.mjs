import { readdir, readFile, writeFile } from "node:fs/promises";
import { resolve } from "node:path";

const root = resolve("content");
const output = resolve(root, "manifest.generated.ts");
const checkOnly = process.argv.includes("--check");

function importName(kind, directory) {
  const pascal = directory.split("-").map((part) => part[0].toUpperCase() + part.slice(1)).join("");
  return `content${kind === "models" ? "Model" : "Material"}${pascal}`;
}

async function scan(kind) {
  const base = resolve(root, kind);
  const entries = await readdir(base, { withFileTypes: true });
  return entries
    .filter((entry) => entry.isDirectory())
    .map((entry) => ({ kind, directory: entry.name }))
    .sort((left, right) => left.directory.localeCompare(right.directory));
}

const entries = [...await scan("models"), ...await scan("materials")];
const imports = entries.map(({ kind, directory }) =>
  `import ${importName(kind, directory)} from "./${kind}/${directory}/index.js";`);
const values = entries.map(({ kind, directory }) => `  ${importName(kind, directory)},`);
const source = [
  "// Generated by scripts/generate-content-manifest.mjs. Do not edit.",
  'import { createContentManifest } from "meshova/pcg";',
  ...imports,
  "",
  "export const contentManifest = createContentManifest([",
  ...values,
  "]);",
  "",
].join("\n");

if (checkOnly) {
  const current = await readFile(output, "utf8").catch(() => "");
  if (current.replaceAll("\r\n", "\n") !== source) {
    throw new Error("content manifest is stale; run pnpm content:manifest");
  }
} else {
  await writeFile(output, source, "utf8");
  console.log(`generated ${entries.length} content entries`);
}
